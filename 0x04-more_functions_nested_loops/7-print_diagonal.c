#include "main.h"
/**
 * print_diagonal - A function that prints diagonal
 * @n: first operand an integer
 * Return: Always 0 (Success)
 */
void print_diagonal(int n)
{
	int i;
	int j;

	for (i = 0; i < n; i++)
	{
		for (j = 0; j < i; j++)
		{
			_putchar(' ');
		}
		_putchar('\\');
		_putchar('\n');
	}
	if (n < 1)
		_putchar('\n');
}
