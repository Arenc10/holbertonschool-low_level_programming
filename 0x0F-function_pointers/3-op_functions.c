#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - Function that adds
 * @a: First operand
 * @b: Second operand
 * Return: Return an int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function that subtracts
 * @a: First operand
 * @b: Second operand
 * Return: Return an int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function that multiplies
 * @a: First operand
 * @b: Second operand
 * Return: Return an int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function that divides
 * @a: First operand
 * @b: Second operand
 * Return: Return an int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Function that shows the reaminder
 * @a: First operand
 * @b: second operand
 * Return: Return an in
 */
int op_mod(int a, int b)
{
	return (a % b);
}
